package JavaBasics;

public class DataTypes1 {

	/**
	 * @param args
	 */
	public static void main(String[] args) {
		
		// 1 int data type
		int i = 10;
		i = 20;
		int j = 30;
		int k = 40;
		System.out.println(j);
		
		// 2 double data type
		double d = 12.33;
		double d1 = 33.33;
		double d2 = 100;
		
		System.out.println(d2);
		// 3 char data type
		char c = 'd';
		char c1 = '1';
		
		// boolean data type
		boolean b1 = true;
		boolean b2 = false;
		System.out.println(b1);
		
		// 4 String is a class not a data type
		String s1 ="sachin";
		String s2 = "sachin2";
		String s3 = "12.33";
		System.out.println(s2);
		System.out.println(s3);

	}

}
